#Comparação: operador de igualdade
#= é um operador de atribuição, por exemplo, a = b atribui a com o valor de b;
#== A pergunta é: esses valores são iguais? então a == b compara a e b.

#Pergunta #1: Qual é o resultado da comparação a seguir?
#2==2
#True - claro, 2 é igual a 2. Python responderá True 
# (lembre-se deste par de literais predefinidos, True e False - eles também são palavras-chave do Python).

#Pergunta #2: Qual é o resultado da comparação a seguir?
#2 == 2.

var = 0 # Atribuindo 0 a var
print(var == 0)

var = 1 # Atribuindo 1 a var
print(var == 0)

#Desigualdade: o operador de desigualdade (!=)
#O operador != (Não é igual a) também compara os valores de dois operandos. 
var = 0  # Atribuindo 0 a var
print(var != 0)

var = 1  # Atribuindo 1 a var
print(var != 0)

